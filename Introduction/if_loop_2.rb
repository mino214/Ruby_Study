# 2. for文に付いて説明します。
# for 変数 in 範囲または配列
#   処理
# end

# 例: 1から5までの数字を表示する (for文)

puts "for文の例です。"
for number in 1..5
  puts "今の数字は#{number} です。"
end

# 3. eachメソッドに付いて説明します。
# eachメソッドは配列や範囲オブジェクトに対して使われます。

# 例: 1から5までの数字を表示する (eachメソッド)

puts "eachメソッドの例です。"
(1..5).each do |number|
  puts "今の数字は#{number} です。"
end

